public class MathAbsExample {
    public static void main(String[] args) {
        System.out.println(Math.abs(5)); // Prints: 5
        System.out.println(Math.abs(-5)); // Prints: 5
        System.out.println(Math.abs(5.0)); // Prints: 5.0
        System.out.println(Math.abs(-5.0)); // Prints: 5.0
    }
}
