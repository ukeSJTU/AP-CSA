package examples;

public class IfThenElseExample {
    public static void main(String[] args) {
        // Example of an if-then-else statement
        boolean hasPrerequisite = true;
        if (hasPrerequisite) {
            System.out.println("Enroll in course");
        } else {
            System.out.println("Enroll in prerequisite");
        }
    }
}
